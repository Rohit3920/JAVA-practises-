
public class OprAssign {

    public static void main(String[] args) {
        int a = 0;

        System.out.println("After value : "+ a + "   ---  Simple assignment operator : " + (a= 10));
        System.out.println("After value : "+ a + "   ---  Simple assignment operator : " + (a+= 10));
        System.out.println("After value : "+ a + "   ---  Simple assignment operator : " + (a-= 10));
        System.out.println("After value : "+ a + "   ---  Simple assignment operator : " + (a*= 10));
        System.out.println("After value : "+ a + "   ---  Simple assignment operator : " + (a/= 10));
        System.out.println("After value : "+ a + "   ---  Simple assignment operator : " + (a%= 10));

    }
}
